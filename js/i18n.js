function interpolate(template, replacements = {}) {
  return template.replace(/\{(\w+)\}/g, (match, key) => {
    return Object.hasOwn(replacements, key) ? String(replacements[key]) : match
  })
}

export function resolveKanbanLanguage(value) {
  return value === "es" || value === "de" ? value : "en"
}

export function createKanbanTranslationGetter(getLanguage) {
  return (key, replacements = {}) => {
    const language = resolveKanbanLanguage(getLanguage())
    const template = translations[language]?.[key] ?? translations.en[key] ?? key
    return interpolate(template, replacements)
  }
}

export function getLocalizedKanbanOptionLabel(group, value, getTranslation, fallbackKey = "common.notSet") {
  if (!value) {
    return getTranslation(fallbackKey)
  }

  const key = optionTranslationKeys[group]?.[value]
  return key ? getTranslation(key) : getTranslation(fallbackKey)
}
